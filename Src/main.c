/****************************************************************
 *
 *	LED support functions
 *
 */
void LED_ON(void)
{
	HAL_GPIO_WritePin(LED_GPIO_Port, LED_Pin, GPIO_PIN_SET);
}
void LED_OFF(void)
{
	HAL_GPIO_WritePin(LED_GPIO_Port, LED_Pin, GPIO_PIN_RESET);
}

/****************************************************************
 *
 *	ADC support functions
 *
 */
void HAL_SYSTICK_Callback(void)
{
	if (Context.ADC_Timeout != 0)		// Check for ADC delay active
	{
		Context.ADC_Timeout--;
		if (Context.ADC_Timeout == 0)	// Check timeout done
		{
			HAL_ADC_Start_IT(&hadc);	// Start conversion
		}
	}
}

void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
	Context.ADC_Value = (uint16_t)HAL_ADC_GetValue(hadc);
	Context.ADC_Timeout = ADC_TIMEOUT;
}

void HAL_ADC_ErrorCallback(ADC_HandleTypeDef *hadc)
{
	Context.Errors |= ADC_ERROR;
}

